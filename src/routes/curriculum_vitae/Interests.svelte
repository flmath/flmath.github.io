        Adam and Bolek play the following game. Automaton generates pair of
        consecutive natural numbers. Then gives one to Adam and the other to
        Bolek. Adam knows the number Bolek got, and Bolek knows the number Adam
        got. But none of them knows the number assigned to themselves. The
        person that has the lower number, pays the number of coins to the other
        person. Each of the players can pass the round, and ask for new pair of
        numbers. Adam thinks: "I can see Bolek has number K, so I have K-1 or
        K+1. If I win I gain K coins, if I lose I give K-1 coins. So on average
        I gain a half of a coin." Boleks reasoning follows the same path. So
        nobody passes. But that would mean, it is not zero sum game.
    </p>
